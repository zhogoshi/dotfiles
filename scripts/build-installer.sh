# ── TUI: arrow-key single-select ─────────────────────────────────────────────
tui_select() {
  local -n _result="$1"; shift
  local prompt="$1";     shift
  local items=("$@")
  local cur=0 total=${#items[@]} key esc

  hide_cursor
  echo -e "  ${BOLD}${prompt}${R}"
  echo -e "  ${DIM}(↑↓ select · Enter confirm)${R}"
  echo ""

  _render_list() {
    for i in "${!items[@]}"; do
      if [ "$i" -eq "$cur" ]; then
        echo -e "  ${CY}${BOLD}▶  ${items[$i]}${R}"
      else
        echo -e "     ${DIM}${items[$i]}${R}"
      fi
    done
  }

  printf '\033[s'
  _render_list

  while true; do
    IFS= read -r -s -n1 key
    if [ "$key" = $'\x1b' ]; then
      IFS= read -r -s -n1 esc
      IFS= read -r -s -n1 esc
      case "$esc" in
        A) [ "$cur" -gt 0 ] && (( cur-- )) ;;
        B) [ "$cur" -lt $(( total - 1 )) ] && (( cur++ )) ;;
      esac
    elif [ "$key" = "" ]; then
      break
    else
      continue
    fi
    printf '\033[u\033[0J'
    _render_list
  done

  printf '\033[u\033[0J'
  show_cursor
  echo -e "  ${GR}${BOLD}✓${R} ${BOLD}${prompt}${R}  ${CY}${items[$cur]}${R}"
  echo ""
  _result="${items[$cur]}"
}

# ── TUI: checkbox multi-select ────────────────────────────────────────────────
tui_checkbox() {
  local -n _checked="$1"; shift
  local prompt="$1";      shift
  local items=("$@")
  local cur=0 total=${#items[@]} key esc
  local selected=()
  for _ in "${items[@]}"; do selected+=(0); done

  hide_cursor
  echo -e "  ${BOLD}${prompt}${R}"
  echo -e "  ${DIM}(↑↓ navigate · Space toggle · Enter continue)${R}"
  echo ""

  _render_cb() {
    for i in "${!items[@]}"; do
      local box="[ ]" col="$DIM"
      [ "${selected[$i]}" -eq 1 ] && box="[${GR}x${R}]" && col=""
      if [ "$i" -eq "$cur" ]; then
        echo -e "  ${CY}${BOLD}▶${R}  ${box} ${col}${items[$i]}${R}"
      else
        echo -e "     ${box} ${col}${DIM}${items[$i]}${R}"
      fi
    done
  }

  printf '\033[s'
  _render_cb

  while true; do
    IFS= read -r -s -n1 key
    if [ "$key" = $'\x1b' ]; then
      IFS= read -r -s -n1 esc
      IFS= read -r -s -n1 esc
      case "$esc" in
        A) [ "$cur" -gt 0 ] && (( cur-- )) ;;
        B) [ "$cur" -lt $(( total - 1 )) ] && (( cur++ )) ;;
      esac
    elif [ "$key" = " " ]; then
      selected[$cur]=$(( 1 - selected[$cur] ))
    elif [ "$key" = "" ]; then
      break
    else
      continue
    fi
    printf '\033[u\033[0J'
    _render_cb
  done

  printf '\033[u\033[0J'
  show_cursor

  _checked=()
  for i in "${!items[@]}"; do
    [ "${selected[$i]}" -eq 1 ] && _checked+=("${items[$i]}")
  done

  if [ ${#_checked[@]} -gt 0 ]; then
    echo -e "  ${GR}${BOLD}✓${R} ${BOLD}${prompt}${R}"
    for item in "${_checked[@]}"; do echo -e "    ${CY}▸${R} ${item}"; done
  else
    echo -e "  ${GR}${BOLD}✓${R} ${BOLD}${prompt}${R}  ${DIM}(none — English only)${R}"
  fi
  echo ""
}

# ── TUI: text input ───────────────────────────────────────────────────────────
tui_input() {
  local -n _inp="$1"; shift
  local prompt="$1" default="${2:-}"
  local hint=""
  [ -n "$default" ] && hint=" ${DIM}[${default}]${R}"
  printf "  ${BOLD}%s${R}%b  " "$prompt" "$hint"
  IFS= read -r _inp
  [ -z "$_inp" ] && _inp="$default"
  echo ""
}

# ── TUI: masked password input ────────────────────────────────────────────────
tui_password() {
  local -n _pw="$1"; shift
  local prompt="$1" default="${2:-}"
  local _char _typed=""
  printf "  ${BOLD}%s${R} ${DIM}(hidden · enter = use default: %s)${R}  " "$prompt" "$default"
  while IFS= read -r -s -n1 _char; do
    if [ -z "$_char" ]; then
      break
    elif [ "$_char" = $'\x7f' ] || [ "$_char" = $'\b' ]; then
      if [ -n "$_typed" ]; then
        _typed="${_typed%?}"
        printf '\b \b'
      fi
    else
      _typed+="$_char"
      printf '*'
    fi
  done
  echo ""
  echo ""
  [ -z "$_typed" ] && _pw="$default" || _pw="$_typed"
}

# ── TUI: yes/no ───────────────────────────────────────────────────────────────
tui_yesno() {
  local -n _yn="$1"; shift
  local choice
  tui_select choice "$1" "Yes" "No"
  [ "$choice" = "Yes" ] && _yn=1 || _yn=0
}

