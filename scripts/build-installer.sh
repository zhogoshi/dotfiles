OUTPUT="$REPO_ROOT/setup-nixos.sh"

# ── Builder UI ────────────────────────────────────────────────────────────────

C_RESET='\033[0m'
C_BOLD='\033[1m'
C_CYAN='\033[36m'
C_GREEN='\033[32m'
C_DIM='\033[2m'

builder_header() {
  echo -e ""
  echo -e "${C_CYAN}${C_BOLD}  ╔══════════════════════════════════════════╗${C_RESET}"
  echo -e "${C_CYAN}${C_BOLD}  ║        NixOS Installer Builder           ║${C_RESET}"
  echo -e "${C_CYAN}${C_BOLD}  ╚══════════════════════════════════════════╝${C_RESET}"
  echo -e ""
}

builder_step() { echo -e "  ${C_CYAN}▶${C_RESET} ${C_BOLD}$1${C_RESET}"; }
builder_ok()   { echo -e "  ${C_GREEN}✓${C_RESET} $1"; }

# ── Nix file embedding ────────────────────────────────────────────────────────
# All paths use $DEST as a RUNTIME variable (not expanded at build time).

emit_nix_file() {
  local rel="$1"
  local abs="$REPO_ROOT/$rel"
  local content
  content="$(cat "$abs")"

  if [ "$rel" = "flake.nix" ]; then
    content="${content/setupMode = false/setupMode = true}"
  fi

  local delim="NIX_EOF_${rel//[^A-Za-z0-9]/_}"
  local dir
  dir="$(dirname "$rel")"

  if [ "$dir" = "." ]; then
    printf 'mkdir -p "$DEST"\n'
  else
    printf 'mkdir -p "$DEST/%s"\n' "$dir"
  fi
  printf 'cat > "$DEST/%s" << '"'"'%s'"'"'\n' "$rel" "$delim"
  printf '%s\n' "$content"
  printf '%s\n\n' "$delim"
}

emit_extra_file() {
  local rel="$1"
  local abs="$REPO_ROOT/$rel"
  [ -f "$abs" ] || return 0
  local delim="EXTRA_EOF_${rel//[^A-Za-z0-9]/_}"
  local dir
  dir="$(dirname "$rel")"

  printf 'mkdir -p "$DEST/%s"\n' "$dir"
  printf 'cat > "$DEST/%s" << '"'"'%s'"'"'\n' "$rel" "$delim"
  cat "$abs"
  printf '%s\n\n' "$delim"
}

# ── Asset embedding ───────────────────────────────────────────────────────────
# For hyprland.conf: patches kb_layout with $KB_LAYOUT after writing.
# All dest paths substitute USERNAME at runtime.

emit_asset_links() {
  local assets_dir="$REPO_ROOT/assets"
  [ -d "$assets_dir" ] || return 0

  cat << 'ASSET_PHASE_HDR'
phase_header "10" "Dotfiles & Assets"
log "Linking asset files..."
mkdir -p "$DEST/assets"
ASSET_PHASE_HDR

  for path_file in "$assets_dir"/*.path; do
    [ -f "$path_file" ] || continue
    local base="${path_file%.path}"
    [ -f "$base" ] || continue

    local orig_dest
    orig_dest="$(tr -d '[:space:]' < "$path_file")"
    local rel_src="assets/$(basename "$base")"
    local delim="ASSET_EOF_$(basename "$base" | tr -c 'A-Za-z0-9' '_')"

    printf 'cat > "$DEST/%s" << '"'"'%s'"'"'\n' "$rel_src" "$delim"
    cat "$base"
    printf '%s\n' "$delim"

    if [ "$(basename "$base")" = "hyprland.conf" ]; then
      printf 'sed -i "s|kb_layout = .*|kb_layout = ${KB_LAYOUT}|" "$DEST/%s"\n' "$rel_src"
      printf 'log "  kb_layout → ${KB_LAYOUT}"\n'
    fi

    printf '_apath="%s"\n' "$orig_dest"
    printf '_apath="${_apath//hogoshi/$USERNAME}"\n'
    printf 'mkdir -p "$(dirname "$_apath")"\n'
    printf 'ln -sf "$DEST/%s" "$_apath"\n' "$rel_src"
    printf 'log "  linked: %s"\n\n' "$rel_src"
  done
}

